package org.cts.housingaid.entity;

public class ComplianceRecord {
    private String complianceRecordEntityId;
    private String complianceRecordId;
    private String complianceRecordType;
    private String complianceRecordResult;
    private String complianceRecordDate;
    private String complianceRecordNotes;

    public String getComplianceRecordEntityId() {
        return complianceRecordEntityId;
    }

    public void setComplianceRecordEntityId(String complianceRecordEntityId) {
        this.complianceRecordEntityId = complianceRecordEntityId;
    }

    public String getComplianceRecordId() {
        return complianceRecordId;
    }

    public void setComplianceRecordId(String complianceRecordId) {
        this.complianceRecordId = complianceRecordId;
    }

    public String getComplianceRecordType() {
        return complianceRecordType;
    }

    public void setComplianceRecordType(String complianceRecordType) {
        this.complianceRecordType = complianceRecordType;
    }

    public String getComplianceRecordResult() {
        return complianceRecordResult;
    }

    public void setComplianceRecordResult(String complianceRecordResult) {
        this.complianceRecordResult = complianceRecordResult;
    }

    public String getComplianceRecordDate() {
        return complianceRecordDate;
    }

    public void setComplianceRecordDate(String complianceRecordDate) {
        this.complianceRecordDate = complianceRecordDate;
    }

    public String getComplianceRecordNotes() {
        return complianceRecordNotes;
    }

    public void setComplianceRecordNotes(String complianceRecordNotes) {
        this.complianceRecordNotes = complianceRecordNotes;
    }


}
